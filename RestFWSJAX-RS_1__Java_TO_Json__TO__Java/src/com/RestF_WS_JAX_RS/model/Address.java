package com.RestF_WS_JAX_RS.model;

public class Address {

	private int flatNo;
	private String buldingName;
	private String areaname;
	/**
	 * @return the flatNo
	 */
	public int getFlatNo() {
		return flatNo;
	}
	/**
	 * @param flatNo the flatNo to set
	 */
	public void setFlatNo(int flatNo) {
		this.flatNo = flatNo;
	}
	/**
	 * @return the buldingName
	 */
	public String getBuldingName() {
		return buldingName;
	}
	/**
	 * @param buldingName the buldingName to set
	 */
	public void setBuldingName(String buldingName) {
		this.buldingName = buldingName;
	}
	/**
	 * @return the areaname
	 */
	public String getAreaname() {
		return areaname;
	}
	/**
	 * @param areaname the areaname to set
	 */
	public void setAreaname(String areaname) {
		this.areaname = areaname;
	}
	
}
